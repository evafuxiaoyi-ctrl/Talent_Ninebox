def normalize_place(value: object) -> str:
    if value is None:
        return ""
    return re.sub(r"[\s/_\-×x,，、|]+", "", str(value).strip()).lower()


def map_ninebox(value: object) -> str | None:
    normalized = normalize_place(value)
    if not normalized:
        return None
    return _DIRECT_MAP.get(normalized)
